class Order {
  constructor(name, telefon, email, location, date, time, eventType, note) {
    this.name = name;
    this.telefon = telefon;
    this.email = email;
    this.location = location;
    this.date = date;
    this.time = time;
    this.eventType = eventType;
    this.note = note;
  }
}

export default Order;
